ExamSchema.index({ sessionStartTime: 1, sessionEndTime: 1 });

// ============================================
// VIRTUALS
// ============================================

ExamSchema.virtual("participantCount").get(function (this: IExam) {
  return this.participants?.length || 0;
});

ExamSchema.virtual("availableSpots").get(function (this: IExam) {
  const maxParticipants = this.maxParticipants || 300;
  const approvedCount =
    this.examPreRegistrationDetails?.filter((reg) => reg.status === "APPROVED")
      .length || 0;
  return Math.max(0, maxParticipants - approvedCount);
});

ExamSchema.virtual("isFull").get(function (this: IExam) {
  return this.availableSpots === 0;
});

ExamSchema.virtual("pendingRegistrationsCount").get(function (this: IExam) {
  return (
    this.examPreRegistrationDetails?.filter((reg) => reg.status === "PENDING")
      .length || 0
  );
});

ExamSchema.virtual("approvedRegistrationsCount").get(function (this: IExam) {
  return (
    this.examPreRegistrationDetails?.filter((reg) => reg.status === "APPROVED")
      .length || 0
  );
});

ExamSchema.virtual("rejectedRegistrationsCount").get(function (this: IExam) {
  return (
    this.examPreRegistrationDetails?.filter((reg) => reg.status === "REJECTED")
      .length || 0
  );
});

// ============================================
// INSTANCE METHODS
// ============================================

ExamSchema.methods.isRegistrationFull = function (this: IExam): boolean {
  return this.isFull || false;
};

ExamSchema.methods.addToWaitingList = async function (
  this: IExam,
  registrationData: Partial<ISessionRegistration>,
): Promise<ISessionRegistration> {
  // Check if already in waiting list
  const existing = this.waitingList?.find(
    (reg) => reg.email === registrationData.email,
  );

  if (existing) {
    throw new Error("Email already registered in waiting list");
  }

  const newRegistration = {
    ...registrationData,
    registeredAt: new Date(),
    status: "PENDING" as WaitingListStatus,
  };

  this.waitingList = this.waitingList || [];
  this.waitingList.push(newRegistration as ISessionRegistration);
  await this.save();

  return newRegistration as ISessionRegistration;
};

ExamSchema.methods.approveWaitingListEntry = async function (
  this: IExam,
  registrationId: string,
): Promise<ISessionRegistration | null> {
  const registration = this.waitingList?.find(
    (reg) => reg._id?.toString() === registrationId,
  );

  if (!registration) {
    throw new Error("Registration not found");
  }

  if (registration.status !== "PENDING") {
    throw new Error("Registration already processed");
  }

  registration.status = "APPROVED";
  registration.approvedAt = new Date();
  await this.save();

  return registration;
};

ExamSchema.methods.rejectWaitingListEntry = async function (
  this: IExam,
  registrationId: string,
): Promise<ISessionRegistration | null> {
  const registration = this.waitingList?.find(
    (reg) => reg._id?.toString() === registrationId,
  );

  if (!registration) {
    throw new Error("Registration not found");
  }

  if (registration.status !== "PENDING") {
    throw new Error("Registration already processed");
  }

  registration.status = "REJECTED";
  await this.save();

  return registration;
};

ExamSchema.methods.admitFromWaitingList = async function (
  this: IExam,
  registrationId: string,
): Promise<IPreRegistrationDetail | null> {
  const waitingEntry = this.waitingList?.find(
    (reg) =>
      reg._id?.toString() === registrationId && reg.status === "APPROVED",
  );

  if (!waitingEntry) {
    throw new Error("No approved waiting list entry found");
  }

  if (this.isFull) {
    throw new Error("Exam is full");
  }

  // Convert to participant
  const participant: IPreRegistrationDetail = {
    sessionId: this.sessionId,
    email: waitingEntry.email,
    emailAddress: waitingEntry.email,
    name: waitingEntry.name,
    courseTaken: waitingEntry.courseTaken,
    level: waitingEntry.level,
    registeredAt: new Date(),
    status: "APPROVED",
    approvalToken: waitingEntry.approvalToken,
    registeredVia: waitingEntry.registeredVia,
    approvedAt: new Date(),
  };

  this.admittedParticipants = this.admittedParticipants || [];
  this.admittedParticipants.push(participant);

  // Also add to participants for backward compatibility
  this.participants.push(participant);

  await this.save();
  return participant;
};

ExamSchema.methods.getWaitingListStats = function (this: IExam) {
  const registrations = this.waitingList || [];
  return {
    total: registrations.length,
    pending: registrations.filter((r) => r.status === "PENDING").length,
    approved: registrations.filter((r) => r.status === "APPROVED").length,
    rejected: registrations.filter((r) => r.status === "REJECTED").length,
  };
};

ExamSchema.methods.checkAccess = function (
  this: IExam,
  email: string,
): {
  hasAccess: boolean;
  isSessionOpen: boolean;
  isTimeReached: boolean;
  message: string;
} {
  const now = new Date();
  const isTimeReached = this.sessionStartTime
    ? now >= this.sessionStartTime
    : true;
  const isSessionOpen = this.isSessionOpen || false;

  // Check if user is approved participant
  const isApproved =
    this.examPreRegistrationDetails?.some(
      (reg) =>
        (reg.email === email || reg.emailAddress === email) &&
        reg.status === "APPROVED",
    ) || false;

  const hasAccess = isApproved && isSessionOpen && isTimeReached;

  let message = "";
  if (!isApproved) message = "Not approved for this session";
  else if (!isSessionOpen) message = "Session is not open yet";
  else if (!isTimeReached) message = "Session time has not been reached";
  else message = "Access granted";

  return { hasAccess, isSessionOpen, isTimeReached, message };
};

// ============================================
// PRE-SAVE MIDDLEWARE
// ============================================

ExamSchema.pre("save", function (this: IExam, next) {
  // Set maxParticipants based on title (GROUP_REVISION sessions have 25 max)
  if (this.title && this.title.toLowerCase().includes("group revision")) {
    if (this.maxParticipants !== 25) {
      this.maxParticipants = 25;
    }
  } else if (!this.maxParticipants) {
    this.maxParticipants = 300;
  }

  // Auto-update status based on date
  const now = new Date();
  if (this.status === "ACTIVE" && this.examDate < now) {
    this.status = "COMPLETED";
  }

  next();
});

